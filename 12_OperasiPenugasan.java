 package pkg_Hari12;

public class Main {
    public static void main(String[] args) {
        
        
        // OPERASI PENUGASAN SEDERHANA
        
        // +=
        // -=
        // *=
        // /=
        // %=
        
        // a = a + 10
        // a = nilai a = 30 + 10
        int a = 30;    
        a += 10;
        System.out.println("a + 10  = "+a+"\n");
        
        // b = b - 20
        // b = 40 - 20
        int b = 40;
        b -= 20;
        System.out.println("b - 20  = "+b+"\n");
        
        // c = c x 5
        // c = 5 x 5
        int c = 5;
        c *= 5;
        System.out.println("c x 5  = "+c+"\n"); 
        
        // d = d : 2
        // d = 50 : 2
        int d = 50;
        d /= 2;
        System.out.println("d : 2 = "+d+"\n"); 
        
        // e = e hasil bagi(%) 8
        // e = 9 % 8
        int e = 9;
        e %= 8;
        System.out.println("e % 8  = "+e);
        
        // Mungkin hanya ini yang bisa pelajari untuk hari ini
        // Mohon dikoreksi kalau ada kekeliruan
        // Terima kasih
    
        
    }
    
}
