 package pkg_Harike14;
public class Main{
    
    public static void main(String[] args) {
        
        // Hari ini kita akan mempelajari tentang 
        // Operasi Pembanding
        // == : Senilai
        // != : tidak sama dengan
        // >  : Lebih dari
        // >= : lebih dari sama dengan
        // <  : kurang dari
        // <= : kurang dari sama dengan
        // Dengan cara dibawah ini :
        
        
        int nilaiA = 10;
        int nilaiB = 10;
        
        System.out.println(nilaiA == nilaiB);
        System.out.println(nilaiA != nilaiB);
        System.out.println(nilaiA > nilaiB);
        System.out.println(nilaiA >= nilaiB);
        System.out.println(nilaiA < nilaiB);
        System.out.println(nilaiA <= nilaiB);
        
        // Mungkin hanya ini yang bisa saya pelajari untuk hari ini
        // jika ada kesalahan mohon dikoreksi 
        // Terima kasih
        
    }
    
}
